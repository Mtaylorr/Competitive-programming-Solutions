typedef long long ll;
const int N = 3e5 + 5;

const int MOD = 1e9 + 7;
template <typename T, typename U>
int mul(T u, U v, int mod = MOD) {
    return ((u % mod) * 1ll * (v % mod)) % mod;
}
template <typename T, typename U>
int add(T u, U v, int mod = MOD) {
    return ((u % mod) + 0ll + (v % mod) + mod) % mod;
}
template <typename T, typename U>
int sub(T u, U v, int mod = MOD) {
    return ((u % mod) + 0ll - (v % mod) + mod) % mod;
}
template <typename T>
int powe(T u, ll v, int mod = MOD) {
    int ans = 1;
    int acc = u % mod;
    while (v) {
        if (v % 2) ans = mul(ans, acc, mod);
        acc = mul(acc, acc, mod);
        v /= 2;
    }
    return ans;
}
template <typename T>
int inv(T u, int mod = MOD) {
    return powe(u, mod - 2, mod);
}

int freq[N];
int n;

void test_case() {
    cin >> n;
    for (int i = 0; i <= n + 1; i++) {
        freq[i] = 0;
    }
    for (int i = 0; i < n; i++) {
        int a;
        cin >> a;
        if (a <= n + 1) {
            freq[a]++;
        }
    }
    ll ans = 0;
    ll cur = 1;
    ll rem = n;
    for (int i = 0; i <= n + 1; i++) {
        rem -= freq[i];
        ans =
            (ans + (i * 1ll * ((cur * 1ll * powe(2, rem)) % MOD)) % MOD) % MOD;
        if (freq[i] == 0) break;
        cur = (cur * 1ll * (sub(powe(2, freq[i]), 1))) % MOD;
    }
    cout << ans << endl;
}

